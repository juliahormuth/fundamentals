// Arredonda p baixo
let num1 = 9.54578
let num2 = Math.floor(num1)

console.log(num2)

// Arredondando p cima
let n1 = 9.54578
let n2 = Math.ceil(num1)

console.log(n2)

// Arrendonda para o mais próximo
let numero1 = 9.49
let numero2 = Math.round(numero1)
console.log(numero2)

// Obter o maior ou menor número
console.log(Math.max(-3, 4, 14, 30))
console.log(Math.min(-3, 4, 14, 30))

// Gerar número aleatório

const aleatorio = Math.random() * (10 - 5) + 5
console.log(aleatorio)

// PI
console.log(Math.PI)

// Elevação
console.log(Math.pow(10, 2))
// OU
console.log(10 ** 2)

//Raiz de um número
let nummer = 9
console.log(nummer ** (1 / 2))